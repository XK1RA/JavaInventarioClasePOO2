    private void JB_REGISTRARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JB_REGISTRARActionPerformed
        // TODO add your handling code here:
        try {
            String Tipo = this.JT_TIPO.getText();
            String Serie = this.JT_SERIE.getText();
            MarcaVO tmp = (MarcaVO) this.JCB_MARCA.getSelectedItem();
            String Marca = ""+tmp.getID();
            int ID = Integer.parseInt(this.JT_ID.getText());
            ImpresoraVO tmpImpr = this.LasImpre.buscarXID(ID);
        if (tmpImpr != null){//control por si agregan una Impresora con un id que ya est√° registrado
                 TE.Msn("Ya hay una Impresora con el ID: " + ID + ", registrada. \n Por favor, ingrese una impresora con otro ID");
        }else{
            if (LasImpre.almacenar(Tipo, ID, Serie, Marca)){
                TE.Msn("Impresora Registrada");
                this.JT_TIPO.setText("");
                this.JT_SERIE.setText("");
                 this.JCB_MARCA.setSelectedIndex(0);
                this.JT_ID.setText("");

            }else{
                TE.Msn("No hay cupo");
            }
        }
        }catch (Exception e){
            TE.Msn("Datos √ârroneos");
        }
    }//GEN-LAST:event_JB_REGISTRARActionPerformed


