    private void JB_REGISTRARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JB_REGISTRARActionPerformed
        // TODO add your handling code here:
        try {
            String Disco = this.JT_DISCO.getText();
            String Memoria = this.JT_MEMORIA.getText();
            String Serie = this.JT_SERIE.getText();
            MarcaVO tmp = (MarcaVO) this.JCB_MARCA.getSelectedItem();
            String Marca = ""+tmp.getID();
            int ID = Integer.parseInt(this.JT_ID.getText());
            CPUVO tmpMoni = this.LosCpu.buscarXID(ID);
        
         if (tmpMoni != null){//control por si agregan una Cpu con un id que ya est√° registrado
                 TE.Msn("Ya hay una CPU con el ID: " + ID + ", registrada. \n Por favor, ingrese una CPU con otro ID");
        }else{
            if (LosCpu.almacenar(Memoria, Disco, ID, Serie, Marca)){
                TE.Msn("Cpu Registrado");
                this.JT_DISCO.setText("");
                this.JT_MEMORIA.setText("");
                this.JT_SERIE.setText("");
                this.JCB_MARCA.setSelectedIndex(0);
                this.JT_ID.setText("");

            }else{
                TE.Msn("No hay cupo");
            }
        }
        }catch (Exception e){
            TE.Msn("Datos √ârroneos");
        }
    }//GEN-LAST:event_JB_REGISTRARActionPerformed


